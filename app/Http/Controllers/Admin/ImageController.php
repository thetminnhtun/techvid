     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
   public function store(Request $request)
    {
        $file = $request->get('image');
        $filename = time() . "_" . explode(".", $request->get('imagename'))[0];

        $filepath = public_path() . '/imgs/uploads/';

        $img = new Image();
        $img->name = $this->save_base64_image($file, $filename, $filepath);
        $sav = $img->save();

        return redirect('admin/image');
    }

    function save_base64_image($file, $filename, $filepath = "")
    {
        $splited = explode(',', substr($file, 5), 2);
        $mime = $splited[0];
        $data = $splited[1];

        $mime_split_without_base64 = explode(';', $mime, 2);
        $mime_split = explode('/', $mime_split_without_base64[0], 2);
        if (count($mime_split) == 2) {
            $extension = $mime_split[1];
            if ($extension == 'jpeg') $extension = 'jpg';
            $filename = $filename . '.' . $extension;
        }
        file_put_contents($filepath . $filename, base64_decode($data));
        return $filename;
    }

